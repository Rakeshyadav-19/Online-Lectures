package com.rax.javabasics.boperators;

public class relationalOperators {
    public static void main(String[] args){
        System.out.println(1 < 2);
        System.out.println(1 > 2);
        System.out.println(1 <= 2);
        System.out.println(1 >= 2);
        System.out.println(1 == 2);
        System.out.println(1 != 2);
    }
}
