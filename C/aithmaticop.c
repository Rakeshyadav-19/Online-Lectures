//to understand basic arithmatic operators.
#include<stdio.h>

int main()
{
    int num1=3,num2=2;

    //basic substraction
   printf("num1-num2= %d\n",num1-num2);
   //performs basic addition
    printf("num1+num2= %d\n",num1+num2);
    //multiplication
    printf("num1*num2= %d\n",num1*num2);
    //division
    printf("num1/num2= %d\n",num1/num2);
    //Modulus=give remainder as output.
    printf("num1 num2= %d",num1%num2);
}