//Learned relational operator.
#include<stdio.h>

int main()
{
    int a=2,b=4;

    printf("a==b =%d\n",a==b);
    //not equal to
    printf("a!=b =%d\n",a!=b);
    //greator than
    printf("a>b =%d\n",a>b);
    //less than
    printf("a<b =%d\n",a<b);
    //greater than or equal to
    printf("a>=b =%d\n",a>=b);
    //less than or equal to
    printf("a<=b =%d\n",a<=b);

}